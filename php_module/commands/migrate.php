<?php
/**
 * Команда миграции данных из старого формата в новый
 */
function migrate($args = []) {
    echo "Проверяем необходимость миграции данных...\n";
    
    if (migrate_old_format()) {
        echo "Миграция выполнена успешно!\n";
    } else {
        echo "Миграция не требуется. Данные уже в новом формате или файл не существует.\n";
    }
}
